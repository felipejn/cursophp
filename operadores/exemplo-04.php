<?php // Operadores de Comparação

$a = 55.0; // float

$b = 55; // int

var_dump($a > $b); // Maior que

echo "<br>";

var_dump($a < $b); // Menor que

echo "<br>";

var_dump($a == $b); // Compara valores iguais

echo "<br>";

var_dump($a === $b); // Compara valores E igualdade de identidade

echo "<br>";

var_dump($a != $b); // Diferente de (valor)

echo "<br>";

var_dump($a !== $b); // Diferente (valor e identidade)

?>
